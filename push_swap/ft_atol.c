#include "push_swap.h"

long int	ft_atol(char *str)
{
	long int	res = 0;
	long int	sign = 1;

	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
		sign = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + *str - '0';
		str++;
	}
	return (sign * res);
}
int	main (int argc, char **argv)
{
	(void)argc;
	printf("converted number: %ld\n", ft_atol(argv[1]));
}
